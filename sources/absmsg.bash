	# ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	if [[ -z "$(which sendemail 2>/dev/null)" ]]; then

		echo "–†–∞–±–æ—Ç–∞ —Å–∫—Ä–∏–ø—Ç–∞ –ø—Ä–µ—Ä–≤–∞–Ω–∞, —Ç.–∫. –≤ —Å–∏—Å—Ç–µ–º–µ –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –ø–∞–∫–µ—Ç sendemail."
