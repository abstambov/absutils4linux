		[[ ${__this[os]} == "ubuntu" ]] && echo "sudo apt install sendemail"

		# 100 - –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –ø–∞–∫–µ—Ç, –æ—Ç –∫–æ—Ç–æ—Ä–æ–≥–æ –∑–∞–≤–∏—Å–∏—Ç —Ä–∞–±–æ—Ç–∞ —Å–∫—Ä–∏–ø—Ç–∞
		return 100
	fi
	if [[ -z "$(which jq 2>/dev/null)" ]]; then

		echo "–†–∞–±–æ—Ç–∞ —Å–∫—Ä–∏–ø—Ç–∞ –ø—Ä–µ—Ä–≤–∞–Ω–∞, —Ç.–∫. –≤ —Å–∏—Å—Ç–µ–º–µ –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –ø–∞–∫–µ—Ç jq."
		[[ ${__this[os]} == "fedora" ]] && echo "sudo dnf install jq"
		[[ ${__this[os]} == "ubuntu" ]] && echo "sudo apt install jq"

		# 100 - –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –ø–∞–∫–µ—Ç, –æ—Ç –∫–æ—Ç–æ—Ä–æ–≥–æ –∑–∞–≤–∏—Å–∏—Ç —Ä–∞–±–æ—Ç–∞ —Å–∫—Ä–∏–ø—Ç–∞
		return 100
	fi


	# 3. –ü–∞—Ä—Å–∏–Ω–≥ –∫–æ–º–∞–Ω–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–∏
	# ~~~~~~~~~~~~~~~~~~~~~~~~~~~
	local -A message=(
		[subject]=${__this[filename]}
		[credentials]=~/.absmsg-credentials
	)

	# –í $temp –ø–æ–º–µ—â–∞–µ–º –æ–±—Ä–∞–±–æ—Ç–∞–Ω–Ω—É—é —Å—Ç—Ä–æ–∫—É —Å –∞—Ä–≥—É–º–µ–Ω—Ç–∞–º–∏ –ø—Ä–∏ –≤—ã–∑–æ–≤–µ —Å–∫—Ä–∏–ø—Ç–∞.
	# –ò–∑–≤–µ—Å—Ç–Ω—ã–µ –∞—Ä–≥—É–º–µ–Ω—Ç—ã (–¥–æ –∞—Ä–≥—É–º–µ–Ω—Ç–∞ "--" ) –±—É–¥—É—Ç –ø–æ–º–µ—â–µ–Ω—ã –≤ –Ω–∞—á–∞–ª–æ —Å—Ç—Ä–æ–∫–∏, –æ—Å—Ç–∞–ª—å–Ω—ã–µ - –ø–æ—Å–ª–µ.
	local temp
	temp=$(getopt --options 'a:c:dehj:lp:s' --longoptions 'attach:,credentials:,debug,email,help,subject:,log,priority:,sms' --name "${__this[filename]}" -- "$@")
	if [ $? -ne 0 ]; then
		# 101 - –æ—à–∏–±–∫–∞ —Ñ—É–Ω–∫—Ü–∏–∏ getopt –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ —Ñ—É–Ω–∫—Ü–∏–∏ main
		echo "–û—à–∏–±–∫–∞ —Ñ—É–Ω–∫—Ü–∏–∏ getopt –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ —Ñ—É–Ω–∫—Ü–∏–∏ main"
		echo "–°–∫—Ä–∏–ø—Ç –∑–∞–≤–µ—Ä—à—ë–Ω —Å –∫–æ–¥–æ–º 101"
		return 101
	fi

	# –ó–∞–º–µ—â–∞–µ–º –∞—Ä–≥—É–º–µ–Ω—Ç—ã —Ñ—É–Ω–∫—Ü–∏–∏ main –Ω–∞ –Ω–æ–≤—ã–µ –∏–∑ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π $temp
	eval set -- "$temp"
	unset temp

	# –í —Ü–∏–∫–ª–µ –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –∏–∑–≤–µ—Å—Ç–Ω—ã–µ –∞—Ä–≥—É–º–µ–Ω—Ç—ã
	while true; do
		case "$1" in
			'-a'|'--attach')
				email[attach]+="-a ${2} "
				shift 2
				continue
			;;
			'-c'|'--credentials')
				message[credentials]="$2"
				shift 2
				continue
			;;
			'-d'|'--debug')
				message[debug]=true
				shift
				continue
			;;
			'-e'|'--email')
				message[email]=true
				shift
				continue
			;;
			'-h'|'--help')
				message[help]=true
				shift
				break
			;;
			'-j'|'--subject')
				message[subject]="$2"
				shift 2
				continue
			;;
			'-l'|'--log')
				message[log]=true
				shift
				continue
			;;
			'-p'|'--priority')
				log[priority]="$2"
				shift 2
				continue
			;;
			'-s'|'--sms')
				message[sms]=true
				shift
				continue
			;;
			'--')
				# "--" - –∫–æ–Ω–µ—Ü –∏–∑–≤–µ—Å—Ç–Ω—ã—Ö –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤
				shift
				break
			;;
			*)
				# 102 - –≤–Ω—É—Ç—Ä–µ–Ω–Ω—è—è –æ—à–∏–±–∫–∞ –ø—Ä–∏ –ø–∞—Ä—Å–∏–Ω–≥–µ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ —Ñ—É–Ω–∫—Ü–∏–∏ main, –≤–æ–∑–º–æ–∂–Ω–æ —Ç—Ä–µ–±—É–µ—Ç—Å—è –æ—Ç–ª–∞–¥–∫–∞
				echo "–í–Ω—É—Ç—Ä–µ–Ω–Ω—è—è –æ—à–∏–±–∫–∞ –ø—Ä–∏ –ø–∞—Ä—Å–∏–Ω–≥–µ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ —Ñ—É–Ω–∫—Ü–∏–∏ main, –≤–æ–∑–º–æ–∂–Ω–æ —Ç—Ä–µ–±—É–µ—Ç—Å—è –æ—Ç–ª–∞–¥–∫–∞"
				echo "–°–∫—Ä–∏–ø—Ç –∑–∞–≤–µ—Ä—à—ë–Ω —Å –∫–æ–¥–æ–º 102"
				return 102
			;;
		esac
	done


	# 4. –ß–∏—Ç–∞–µ–º .absmsg-credentials
	# ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

	# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –∞—Å—Å–æ—Ü–∏–∞—Ç–∏–≤–Ω—ã—Ö –º–∞—Å—Å–∏–≤–æ–≤ email –∏ sms
	if [[ -r "${message[credentials]}" ]]; then
		. "${message[credentials]}"
		if [[	-z "${email[from]}"   || -z "${email[to]}"     || -z "${email[smtp_address]}" || -z "${email[smtp_username]}" || -z "${email[smtp_password]}" || \
				-z "${sms[username]}" || -z "${sms[password]}" || -z "${sms[url]}"            || -z "${sms[msisdn]}"          || -z "${sms[shortcode]}" ]]; then
			# 104 - –æ—à–∏–±–∫–∞: ${message[credentials]} —Å–æ–¥–µ—Ä–∂–∏—Ç –Ω–µ –ø–æ–ª–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
			echo "–û—à–∏–±–∫–∞: ${message[credentials]} —Å–æ–¥–µ—Ä–∂–∏—Ç –Ω–µ –ø–æ–ª–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ"
			echo "–°–∫—Ä–∏–ø—Ç –∑–∞–≤–µ—Ä—à—ë–Ω —Å –∫–æ–¥–æ–º 104"
			return 104
		fi
	else
		# 103 - –æ—à–∏–±–∫–∞: ${message[credentials]} –Ω–µ –¥–æ—Å—Ç—É–ø–µ–Ω –¥–ª—è —á—Ç–µ–Ω–∏—è
		echo "–û—à–∏–±–∫–∞: ${message[credentials]} –Ω–µ –¥–æ—Å—Ç—É–ø–µ–Ω –¥–ª—è —á—Ç–µ–Ω–∏—è"
		echo "–°–∫—Ä–∏–ø—Ç –∑–∞–≤–µ—Ä—à—ë–Ω —Å –∫–æ–¥–æ–º 103"
		return 103
	fi


	# 5. –§–æ—Ä–º–∏—Ä—É–µ–º —Ç–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è
	# ~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	local -a message_strings
	local error_read_file

	if [[ "${message[help]}" != "true" ]]; then
		# –ï—Å–ª–∏ –¥–µ—Å–∫—Ä–∏–ø—Ç–æ—Ä stdin –æ—Ç–∫—Ä—ã—Ç –Ω–µ –≤ —Ç–µ—Ä–º–∏–Ω–∞–ª–µ (—á–µ—Ä–µ–∑ pipe), —Å–º. 'man test' - —á–∏—Ç–∞–µ–º —Ç–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è –∏–∑ pipe –≤ –º–∞—Å—Å–∏–≤ —Å—Ç—Ä–æ–∫
		[[ ! -t 0 ]] && mapfile message_strings
		# –í—Å–µ –æ—Å—Ç–∞–≤—à–∏–µ—Å—è –ø–æ—Å–ª–µ —Ä–∞–∑–±–æ—Ä–∞ –∞—Ä–≥—É–º–µ–Ω—Ç—ã –∫–æ–º–∞–Ω–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–∏ —Ä–∞—Å—Å–º–∞—Ç—Ä–∏–≤–∞–µ–º, –∫–∞–∫ –∏–º–µ–Ω–∞ —Ñ–∞–π–ª–æ–≤ —Å —á–∞—Å—Ç—è–º–∏ –æ–¥–Ω–æ–≥–æ –±—É–¥—É—â–µ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è
		while [[ $# -gt 0 ]] do
			[[ "$1" == "--" ]] && continue
			if [[ -s "$1" ]]; then
				# –ü–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ –¥–æ–±–∞–≤–ª—è–µ–º –∫–∞–∂–¥—ã–π —Ñ–∞–π–ª –∫ —Å–æ–æ–±—â–µ–Ω–∏—é
				mapfile -O "${#message_strings[@]}" message_strings < "$1"
				shift
			else
				# –ï—Å–ª–∏ —Ö–æ—Ç—å –æ–¥–∏–Ω —Ñ–∞–π–ª –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –∏–ª–∏ –∏–º–µ–µ—Ç –Ω—É–ª–µ–≤—É—é –¥–ª–∏–Ω—É, —Ç–æ –±—É–¥–µ—Ç –≤—ã—Ö–æ–¥ –ø–æ –æ—à–∏–±–∫–µ —Å –ø–æ–∫–∞–∑–æ–º –ø–æ–¥—Å–∫–∞–∑–∫–∏.
				message[help]=true
				error_read_file="$1"
				break
			fi
		done
	fi
	# –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤–æ–æ–±—â–µ –Ω–µ –∑–∞–¥–∞–ª —Ç–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è, —Ç–æ –≤—ã—Å—ã–ª–∞–µ–º –≤—Ä–µ–º–µ–Ω–Ω—É—é –º–µ—Ç–∫—É
	[[ ${#message_strings[@]} -eq 0 ]] && message_strings=( "$(whoami) on $(hostname) at $(date)" )


	# 6. –û—Å–Ω–æ–≤–Ω–∞—è —á–∞—Å—Ç—å
	# ~~~~~~~~~~~~~~~~~

	# –ï—Å–ª–∏ –±—ã–ª–∞ –∑–∞–ø—Ä–æ—à–µ–Ω–∞ –ø–æ–º–æ—â—å (–∏–ª–∏ –±—ã–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ —á—Ç–µ–Ω–∏–∏ —Ñ–∞–π–ª–æ–≤ —à–∞–≥–æ–º —Ä–∞–Ω–µ–µ)
	if [[ "${message[help]}" == "true" ]]; then
		echo "${__this[filename]}   version 0.1, ¬© 2024 by Sergey Vasiliev aka abs."
		echo "${__this[filename]} - —Å–∫—Ä–∏–ø—Ç –¥–ª—è —É–ø—Ä–æ—â–µ–Ω–Ω–æ–π –æ–¥–Ω–æ–≤—Ä–µ–º–µ–Ω–Ω–æ–π –æ—Ç–ø—Ä–∞–≤–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏–π –ø–æ —ç–ª–µ–∫—Ç—Ä–æ–Ω–Ω–æ–π –ø–æ—á—Ç–µ, –°–ú–° –∏ –≤ —Å–∏—Å—Ç–µ–º–Ω—ã–π –∂—É—Ä–Ω–∞–ª. –¢–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è –±–µ—Ä—ë—Ç—Å—è –∏–∑ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–≥–æ –ø–æ—Ç–æ–∫–∞ stdin –∏ —Å–æ–¥–µ—Ä–∂–∏–º–æ–≥–æ —Ñ–∞–π–ª–æ–≤ - –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ –∫–æ–º–∞–Ω–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–∏. –í—Å–µ –Ω–µ —Ä–∞–∑–æ–±—Ä–∞–Ω–Ω—ã–µ –æ–ø—Ü–∏–∏ —Å—á–∏—Ç–∞—é—Ç—Å—è –∏–º–µ–Ω–∞–º–∏ —Ñ–∞–π–ª–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ –¥–æ–±–∞–≤–ª—è—é—Ç—Å—è –∫ –∏—Ç–æ–≥–æ–≤–æ–º—É —Å–æ–æ–±—â–µ–Ω–∏—é. –ï—Å–ª–∏ —Å–æ–æ–±—â–µ–Ω–∏–µ –≤–æ–æ–±—â–µ –Ω–µ –∑–∞–¥–∞–Ω–æ, —Ç–æ –≤ —Å–æ–æ–±—â–µ–Ω–∏–µ –∑–∞–ø–∏—Å—ã–≤–∞–µ—Ç—Å—è –∏–º—è —Ç–µ–∫—É—â–µ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è, –∏–º—è —Ö–æ—Å—Ç–∞, –≤—Ä–µ–º—è –∏ –¥–∞—Ç–∞."
		echo ""
		echo "${__this[filename]} comes with ABSOLUTELY NO WARRANTY. This is free software, and you are welcome to redistribute it under certain conditions.  See  the GNU General Public Licence for details."
		echo ""
		echo "–ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: ${__this[filename]} [–û–ü–¶–ò–Ø] [--] [FILE1] [FILE2] ..."
		echo ""
		echo "–û–ø—Ü–∏–∏:"
		echo "-e, --email                        –æ—Ç–ø—Ä–∞–≤–∏—Ç—å —Å–æ–æ–±—â–µ–Ω–∏–µ –ø–æ —ç–ª–µ–∫—Ç—Ä–æ–Ω–Ω–æ–π –ø–æ—á—Ç–µ"
		echo "-j, --subject \"–¢–µ–º–∞ –ø–∏—Å—å–º–∞\"        —Ç–µ–º–∞ –ø–∏—Å—å–º–∞; –ø–æ-—É–º–æ–ª—á–∞–Ω–∏—é –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –∏–º—è —Å–∫—Ä–∏–ø—Ç–∞ '${__this[filename]}'; –æ–ø—Ü–∏—è –∏–≥–Ω–æ—Ä–∏—Ä—É–µ—Ç—Å—è –ø—Ä–∏ –≤—ã–±–æ—Ä–µ —Å–ø–æ—Å–æ–±–∞ –æ—Ç–ø—Ä–∞–≤–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏–π sms"
		echo "-a, --attach \"–ò–º—è —Ñ–∞–π–ª–∞\"           –≤–ª–æ–∂–µ–Ω–∏–µ –∫ –ø–∏—Å—å–º—É: –∏–º—è —Ñ–∞–π–ª–∞ —Å –ø—É—Ç—ë–º; –æ–ø—Ü–∏—è –º–æ–∂–µ—Ç –±—ã—Ç—å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∞ –º–Ω–æ–≥–æ–∫—Ä–∞—Ç–Ω–æ; –æ–ø—Ü–∏—è –∏–≥–Ω–æ—Ä–∏—Ä—É–µ—Ç—Å—è –ø—Ä–∏ –≤—ã–±–æ—Ä–µ –¥—Ä—É–≥–∏—Ö —Å–ø–æ—Å–æ–±–æ–≤ –æ—Ç–ø—Ä–∞–≤–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏–π"
		echo ""
		echo "-l, --log                          —Å–¥–µ–ª–∞—Ç—å –∑–∞–ø–∏—Å—å –≤ —Å–∏—Å—Ç–µ–º–Ω–æ–º –∂—É—Ä–Ω–∞–ª–µ"
		echo "-j, --subject \"–¢–µ–≥\"                —Ç–µ–≥ –∑–∞–ø–∏—Å–∏ –≤ —Å–∏—Å—Ç–µ–º–Ω–æ–º –∂—É—Ä–Ω–∞–ª–µ; –ø—Ä–æ–±–µ–ª—ã –ø—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω–æ –º–µ–Ω—è—é—Ç—Å—è –Ω–∞ –∑–Ω–∞–∫–∏ –ø–æ–¥—á—ë—Ä–∫–∏–≤–∞–Ω–∏—è '_'; –µ—Å–ª–∏ —Ç–µ–≥ –Ω–µ –∑–∞–¥–∞–Ω, —Ç–æ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –∏–º—è —Å–∫—Ä–∏–ø—Ç–∞ '${__this[filename]}'; –æ–ø—Ü–∏—è –∏–≥–Ω–æ—Ä–∏—Ä—É–µ—Ç—Å—è –ø—Ä–∏ –≤—ã–±–æ—Ä–µ —Å–ø–æ—Å–æ–±–∞ –æ—Ç–ø—Ä–∞–≤–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏–π sms"
		echo "-p, --priority \"facility.level\"    –æ–ø–∏—Å–∞–Ω–∏–µ –≤–æ–∑–º–æ–∂–Ω—ã—Ö –∫–æ–º–±–∏–Ω–∞—Ü–∏–π –∏–º—ë–Ω facility –∏ level —Å–º–æ—Ç—Ä–∏—Ç–µ 'man logger'; –ø–æ-—É–º–æ–ª—á–∞–Ω–∏—é –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è user.info; –æ–ø—Ü–∏—è –∏–≥–Ω–æ—Ä–∏—Ä—É–µ—Ç—Å—è –ø—Ä–∏ –≤—ã–±–æ—Ä–µ –¥—Ä—É–≥–∏—Ö —Å–ø–æ—Å–æ–±–æ–≤ –æ—Ç–ø—Ä–∞–≤–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏–π"
		echo ""
		echo "-s, --sms                          –æ—Ç–ø—Ä–∞–≤–∏—Ç—å –°–ú–°"
		echo ""
		echo "-c, --credentials \"–ò–º—è —Ñ–∞–π–ª–∞\"      –ø—É—Ç—å –∫ —Ñ–∞–π–ª—É —Å –ø—Ä–∏–≤–∞—Ç–Ω—ã–º–∏ –¥–∞–Ω–Ω—ã–º–∏ –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏–π –ø–æ —ç–ª–µ–∫—Ç—Ä–æ–Ω–Ω–æ–π –ø–æ—á—Ç–µ –∏ –°–ú–°; –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é - ~/.absmsg-credentials"
		echo "-d, --debug                        —Ä–µ–∂–∏–º –æ—Ç–ª–∞–¥–∫–∏ –¥–ª—è –≤—ã–≤–æ–¥–∞ –¥–∏–∞–≥–Ω–æ—Å—Ç–∏—á–µ—Å–∫–∏—Ö —Å–æ–æ–±—â–µ–Ω–∏–π –∏—Å–ø–æ–ª—å–∑—É–µ–º—ã—Ö –∫–æ–º–∞–Ω–¥"
		echo "-h, --help                         –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç —ç—Ç—É –ø–æ–¥—Å–∫–∞–∑–∫—É"
		echo ""
		echo "–ü—Ä–∏–º–µ—Ä—ã –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è:"
		echo "echo \"–°–æ–æ–±—â–µ–Ω–∏–µ\" | ${__this[filename]} --log --email"
		echo "cat /etc/passwd | ${__this[filename]} --email --subject \"–§–∞–π–ª passwd\""
		echo "${__this[filename]} --email --subject \"–§–∞–π–ª passwd\" </etc/passwd"
		echo "echo \"–°–æ–æ–±—â–µ–Ω–∏–µ\" | ${__this[filename]} --log --email --subject \"–ü–∏—Å—å–º–æ —Å –≤–ª–æ–∂–µ–Ω–∏—è–º–∏\" --attach \"/etc/passwd\" --attach /etc/group -d"
		echo "echo \"–°–æ–æ–±—â–µ–Ω–∏–µ\" | ${__this[filename]} --log --subject \"–ö–∞–∫–æ–π-—Ç–æ –∑–∞–≥–æ–ª–æ–≤–æ–∫\" --priority cron.err"
		echo "echo \"–°–æ–æ–±—â–µ–Ω–∏–µ\" | ${__this[filename]} -e -j \"–ö–∞–∫–æ–π-—Ç–æ –∑–∞–≥–æ–ª–æ–≤–æ–∫\" -d ~/.nanorc ~/.bashrc"
		echo "${__this[filename]} -e -j \"–ö–∞–∫–æ–π-—Ç–æ –∑–∞–≥–æ–ª–æ–≤–æ–∫\" -- ~/.nanorc ~/.bashrc"
		echo "${__this[filename]} -el"
		echo "echo \"–°–æ–æ–±—â–µ–Ω–∏–µ\" | ${__this[filename]} --sms"


		# –ë—ã–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ —á—Ç–µ–Ω–∏–∏ —Ñ–∞–π–ª–∞ –∏–∑ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ –≤ –∫–æ–º–∞–Ω–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–µ
		if [[ -n "$error_read_file" ]]; then
			# 105 - –æ—à–∏–±–∫–∞ –ø—Ä–∏ —á—Ç–µ–Ω–∏–∏ —Ñ–∞–π–ª–∞ –∏–∑ –∫–æ–º–∞–Ω–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–∏
			echo ""
			echo ""
			echo "–û—à–∏–±–∫–∞ –ø—Ä–∏ —á—Ç–µ–Ω–∏–∏ —Ñ–∞–π–ª–∞ ${error_read_file}"
			echo "–§–∞–π–ª –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –∏–ª–∏ –∏–º–µ–µ—Ç –Ω—É–ª–µ–≤—É—é –¥–ª–∏–Ω—É"
			echo "–°–∫—Ä–∏–ø—Ç –∑–∞–≤–µ—Ä—à—ë–Ω —Å –∫–æ–¥–æ–º 105"
			return 105
		fi

		return 0;
	fi

	# –í–∫–ª—é—á–∞–µ–º —Ä–µ–∂–∏–º –æ—Ç–ª–∞–¥–∫–∏ –ø–æ –∑–∞–ø—Ä–æ—Å—É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
	[[ "${message[debug]}" == "true" ]] && email[debug]="-v" && sms[debug]="--verbose" && set -x

	# –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –ø–æ —ç–ª–µ–∫—Ç—Ä–æ–Ω–Ω–æ–π –ø–æ—á—Ç–µ
	[[ "${message[email]}" == "true" ]] && eval sendemail -f "${email[from]}" -t "${email[to]}" -o message-charset=utf-8 -s "${email[smtp_address]}" -xu "${email[smtp_username]}" -xp "${email[smtp_password]}" "${email[debug]}" -u \'"${message[subject]}"\' -m \'"${message_strings[@]}"\' ${email[attach]}
	# ... –≤ —Å–∏—Å—Ç–µ–º–Ω—ã–π –∂—É—Ä–Ω–∞–ª
	[[ "${message[log]}"   == "true" ]] && printf "%s" "${message_strings[@]}" | logger -t "${message[subject]// /_}" --priority "${log[priority]}" --
	# ... –ø–æ –°–ú–°
	if [[ "${message[sms]}" == "true" ]]; then
		# –û—Ç–ø—Ä–∞–≤–∫–∞ –°–ú–°
		sms[text]="${message_strings[*]//$'\n'/ }"
		local result1=$(eval curl "${sms[POST]} ${sms[debug]}")
		local result2=""
		# –ï—Å–ª–∏ –±—ã–ª –∑–∞–ø—Ä–æ—à–µ–Ω —Ä–µ–∂–∏–º –æ—Ç–ª–∞–¥–∫–∏,—Ç–æ –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–æ –¥–µ–ª–∞–µ–º –∑–∞–ø—Ä–æ—Å –æ —Å–æ—Å—Ç–æ—è–Ω–∏–∏ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–Ω–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è
		if [[ "${message[debug]}" == "true" ]]; then
			local message_id=$(echo "$result1" | jq --raw-output ".result.uid")
			result2=$(eval curl "${sms[GET]}${message_id} ${sms[debug]}")
			set +x
		fi
		# –§–∏–∫—Å–∏—Ä—É–µ–º —Ñ–∞–∫—Ç –æ—Ç–ø—Ä–∞–≤–∫–∏ –∏ –≤—Å–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –≤ —Å–∏—Å—Ç–µ–º–Ω–æ–º –∂—É—Ä–Ω–∞–ª–µ
		logger -t "${__this[filename]}" --priority "user.info" -- --------------------
		logger -t "${__this[filename]}" --priority "user.info" -- SMS send to Tele2 by "$(whoami) on $(hostname) at $(date)"
		logger -t "${__this[filename]}" --priority "user.info" -- Command: $(eval echo "curl ${sms[POST]} ${sms[debug]}")
		logger -t "${__this[filename]}" --priority "user.info" -- Result : "$result1"
		if [[ "${sms[debug]}" == "--verbose" ]]; then
			logger -t "${__this[filename]}" --priority "user.info" -- Command: $(eval echo "curl ${sms[GET]}${message_id} ${sms[debug]}")
			logger -t "${__this[filename]}" --priority "user.info" -- Result : "$result2"
		fi
	fi

	# –í—ã–∫–ª—é—á–∞–µ–º —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–Ω—ã–π —Ä–µ–∂–∏–º –æ—Ç–ª–∞–¥–∫–∏
	[[ "${message[debug]}" == "true" ]] && set +x

	return 0;
} # main



