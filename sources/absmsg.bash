# -----------------------------------------------------------------------------
function get_os {
	local os=$(hostnamectl | grep 'Operating System: ' | awk '{print $3}')
	if [[ "$os" == "Ubuntu" ]]; then
		echo "ubuntu"
	elif [[ "$os" == "Fedora" ]]; then
		echo "fedora"
	else
		echo "$os"
	fi
	return 0
} # get_os



